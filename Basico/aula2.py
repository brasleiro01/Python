# Tipos int e float
# int -> Número inteiro
# positivo ou negativo
print(11)
print(-11)
print(0)

# float -> Número com ponto flutuante
# positivo ou negativo com ponto flutuante
print(1.1)
print(2.25)
print(10.5, 10.10)

# A função type mostra o tipo que o python inferiu ao valor
print(type('Marcus'))