"""
Formação basica de strings
s - string
d - int
f - float
.<número de digitos>f
x ou x - Hexadecimal
(Caractere) (><^) (quantidade)
> - esquerda
< - direita
^ - centro
Sinal - + ou -
Ex.: 0>-100,.1f
conversion flags - !r !s !a
"""

variavel = 'ABC'
print(f'{variavel}')
print(f'{variavel: >10}')
print(f'{variavel: <10}')
